   /***************************************************************************
	 函数名称：
	 功    能：返回n阶legendre多项式的值
	 输入参数：
	 返 回 值：
	 说    明：函数名、形参、返回类型均不准动
   ***************************************************************************/
double legendre(double x, int n)
{
	if (n == 0)
		return 1;
	else if (n == 1)
		return x;
	else
	{
		double a = 0;
		a = ((2 * n - 1) * x * legendre(x, n - 1) - (n - 1) * legendre(x, n - 2)) / n;
		return a;
	}
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：完成输入、调用递归函数、输出
***************************************************************************/
int main()
{
	cout << "计算legendre,请输入x和n的值" << endl;
	double x;
	int n;
	cin >> x >> n;
	double ans;
	ans = legendre(x, n);
	cout << "legendre[" << n << "](" << x << ")=" << ans<<endl;
	return 0;
}


